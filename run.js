const lines = rawText.split('\n').filter(l => l.trim().length > 0);
let allHoles = [];

for (const line of lines) {
  const parts = line.trim().split(/\s+/);
  if (parts.length >= 6) {
    let rawId = parts[1];
    let depth = parseFloat(parts[2].replace(',', '.'));
    let block = parseInt(parts[5]);
    
    let location = 'MD';
    if (block >= 14 && block <= 17) location = 'VT';
    if (block >= 18 && block <= 22) location = 'ME';

    let match = rawId.match(/^(\d+)([A-E])(\d+)(P|S)$/);
    if (!match) match = rawId.match(/(\d+)([A-E])(\d+)(P|S)/);
    
    if (match) {
        allHoles.push({
            id: rawId,
            depth,
            block,
            location,
            line: match[2],
            isPrimary: match[4] === 'P',
            typeSuffix: match[4]
        });
    }
  }
}

